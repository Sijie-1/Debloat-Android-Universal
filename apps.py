def verificar_conexion_adb():
    resultado = subprocess.run(["adb", "devices"], capture_output=True, text=True)
    lineas = resultado.stdout.strip().split('\n')
    
    if len(lineas) <= 1:
        print("[ERROR] No se detectaron dispositivos ADB conectados.")
        sys.exit(1)
        
    print("[SISTEMA] Dispositivo ADB validado. Iniciando transferencia de comandos.")

def purgar_paquetes_unificados():
    total_paquetes = len(PAQUETES_A_DESINSTALAR)
    print(f"[SISTEMA] Se procesará una carga de {total_paquetes} identificadores.")
    
    for indice, paquete in enumerate(PAQUETES_A_DESINSTALAR, start=1):
        comando = ["adb", "shell", "pm", "uninstall", "--user", "0", paquete]
        
        try:
            proceso = subprocess.run(comando, capture_output=True, text=True, timeout=10)
            salida_consola = proceso.stdout.strip()
            
            if "Success" in salida_consola:
                print(f"[{indice}/{total_paquetes}] [ÉXITO] Componente eliminado: {paquete}")
            else:
                salida_error = salida_consola or proceso.stderr.strip()
                print(f"[{indice}/{total_paquetes}] [OMITIDO] Operación denegada en {paquete}: {salida_error}")
                
        except subprocess.TimeoutExpired:
            print(f"[{indice}/{total_paquetes}] [TIEMPO AGOTADO] El puente de depuración no respondió al procesar {paquete}.")
        except Exception as error_critico:
            print(f"[{indice}/{total_paquetes}] [ERROR FATAL] Fallo de subproceso en {paquete}: {error_critico}")

def principal():
    verificar_conexion_adb()
    purgar_paquetes_unificados()
    print("\n[SISTEMA] Secuencia operativa finalizada.")

if __name__ == "__main__":
    principal()